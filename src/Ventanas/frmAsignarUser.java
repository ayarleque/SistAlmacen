    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        /*
            PARA EL NIVEL DEL USUARIO
                1: PARA EL ING. INFORMATICO O QUIEN CONTROLE EL SISTEMA DESDE LA PROGRAMACIÓN
                2: PARA EL ENCARGADO DEL ALMACEN
                3: PARA EL GESTOR FUNCIONAL
                4: PARA EL RESPONSABLE FINANCIERO
        
            VER DIAGRAMA DE CASOS DE USO PARA SABER LAS FUNCIONES DE CADA ACTOR
        */
        //JOptionPane.showMessageDialog(null, nombre[cboTrabajador.getSelectedIndex()]+apell[cboTrabajador.getSelectedIndex()]);
        
        if (cboCat.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null,"Por favor, seleccione el cargo del trabajador");
        }
        else{
            String nomUser;
            CallableStatement cst;
            Connection con=null;
            try{
                con=Conexion.getconnection();
                nomUser=(nombre[cboTrabajador.getSelectedIndex()]+apell[cboTrabajador.getSelectedIndex()]).toLowerCase();    
                cst=con.prepareCall("{call paIngresaUser (?,?,?)}");
                cst.setString(1,nomUser);
                cst.setInt(2,idTrab[cboTrabajador.getSelectedIndex()]);
                cst.setInt(3,cboCat.getSelectedIndex()+1);
                cst.execute();
                JOptionPane.showMessageDialog(null,"Usuario asignado correctamente\nSu nombre de usuario es: "+nomUser+"\nSu contraseña es: "+nomUser+"p18");
                cst.close();
                con.close();
                this.dispose();
            }
            catch (SQLException ex){
                JOptionPane.showMessageDialog(null,"¡Error al intentar guardar los datos!\nSi el trabajadador anteriormente se le asignó un usuario,\nContáctese con el área de Informática"
                        + "\nError: "+ ex);
            }
        }     
    }//GEN-LAST:event_btnGuardarActionPerformed

    
